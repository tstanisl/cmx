#define INF INFINITY

#define LOG(...) fprintf(stderr, __VA_ARGS__)

#define CHECK(cnd, ...) \
    if (cnd); else (LOG(__VA_ARGS__), exit(-1))

typedef struct {
    float x;
    float y;
    uint8_t r;
} tc_s;

#define TEST(type, testcases) do {                                    \
    LOG("Testing " #type "\n");                                       \
    for (size_t i = 0; i < sizeof(testcases) / sizeof(tc_s); ++i) {   \
        tc_s t = (testcases)[i];                                      \
        type m = type ## _encode(t.x);                                \
        float z = type ## _decode(m);                                 \
        CHECK(m.r == t.r, "\tError: encode: %g -> %08b (got %08b/%g)\n", \
                           t.x, t.r, m.r, z);                         \
        CHECK((isnan(z) && isnan(t.y)) ||                             \
              z == t.y,   "\tError: decode: %g -> %g (got %g)\n",     \
                          t.x, t.y, z);                               \
    }                                                                 \
    LOG("\tPassed\n");                                                \
} while (0)

